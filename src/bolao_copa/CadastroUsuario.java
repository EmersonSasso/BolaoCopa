    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        aux_txt = "";
        if ("editar".equals(saveButton.getText().toLowerCase()) && comboUser.getSelectedIndex() == 0) {
            aux_txt = "Escolha um Usuário!\n";
        }
        if (comboGrupoPrivil.getSelectedIndex() == 0) {
            aux_txt = aux_txt + "Escolha um grupo!\n";
        }
        if ("".equals(nameField.getText())) {
            aux_txt = aux_txt + "Preencha o campo nome!\n";
        }
        if ("".equals(loginField.getText())) {
            aux_txt = aux_txt + "Preencha o campo login!\n";
        }
        if (checkSenha.isSelected()) {
            if ("".equals(met.passToString(passwordField.getPassword()))) {
                aux_txt = aux_txt + "Preencha o campo senha!\n";
            }
        }
        try {
            if (!"".equals(aux_txt)) {
                JOptionPane.showMessageDialog(this, "Por favor verifique as inconsistencias:\n" + aux_txt);
            } else {
                aux_txt = metCadUs.doCadastro(Integer.parseInt(comboGrupoPrivil.getSelectedItem().toString().split(" - ")[0]),
                        nameField.getText().toUpperCase(), loginField.getText().toUpperCase(), met.passToString(passwordField.getPassword()),
                        checkAtivo.isSelected(), checkSenha.isSelected(), "salvar".equals(saveButton.getText().toLowerCase()));
                if ("0".equals(aux_txt)) {
                    aux_txt = "Usuário "
                            + loginField.getText().toUpperCase()
                            + " existente!";
                } else if ("salvar".equals(saveButton.getText().toLowerCase())) {
                    aux_txt = "Usuário "
                            + loginField.getText().toUpperCase()
                            + " criado com o código: " + aux_txt;
                } else {
                    aux_txt = "Usuário "
                            + loginField.getText().toUpperCase()
                            + ", referente ao codigo " + aux_txt + " editado!";
                }
                JOptionPane.showMessageDialog(this, aux_txt);
                Limpa();
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

