    public void setTipo(boolean editar) {
        try {
            checkAtivo.setVisible(editar);
            checkSenha.setVisible(editar);
            checkSenha.setSelected(!editar);
            passwordField.setEnabled(!editar);
            comboUser.setVisible(editar);
            labelUser.setVisible(editar);
            loginField.setEnabled(!editar);
            if (editar) {
                saveButton.setText("Editar");
                met.makeJCBUser(comboUser);
            } else {
                saveButton.setText("Salvar");
                checkAtivo.setSelected(!editar);
            }
            if (!first) {
                met.makeJCBUser(comboUser);
                met.makeJCBGrupo(comboGrupoPrivil);
                first = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void Limpa() {
        comboGrupoPrivil.setSelectedIndex(0);
        comboUser.setSelectedIndex(0);
        nameField.setText("");
        passwordField.setText("");
        checkAtivo.setSelected(false);
        if ("salvar".equals(saveButton.getText().toLowerCase())) {
            checkSenha.setSelected(false);
        } else {
            checkSenha.setSelected(true);
        }
        checkSenha.doClick();
        loginField.setText("");
        aux_txt = "";
    }
